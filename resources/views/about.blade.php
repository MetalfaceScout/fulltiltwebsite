        <h1>About FULL TILT</h1>
        <h2>Who we are</h2>
        <p>We're just a group of highschool friends looking to make some music. Currently Full Tilt consists of:</p>
          <ul>
            <li>Jackson Rolfsness - Drummer</li>
            <li>Josh Mathis - Guitarist</li>
            <li>TJ Carter - Guitarist</li>
            <li>Chad Roundy - Bassist</li>
            <li>Logan Mathis - Vocalist</li>
          </ul>
        <p>We enjoy everything from live music to occasional video games, and are always looking to have a good time. Our favorite thing to do is just jam out together and have fun making music.</p>
        <h2>Where we came from</h2>
        <p>We all come from St. George and have lived here for a while. The scenery and the town are always an inspiration to us while we write. Each of us grew up listening to metal music, so it only makes sense we'd end up in a metal band.</p>
        <h2>What we want to do</h2>
        <p>Every band wants to make it big, but we at least want to perform live around town and in Cedar City. We want to write a few albums and bring them to you live on stage. We also want to do a number of free concerts out in the desert. We're working on our first album, but for our next album we want to drop the tuning and make it even heavier. We wouldn't really mind if Full Tilt stays local forever.
        <h2>What we're doing now</h2>
        <p>We meet up for practice every week and write music. At the time of writing this, we just recorded our first single, Full Tilt. It's an awesome thrashy track that we're all proud of. We also have 9 more songs in the lineup ready to be recorded for the album. We still haven't decided what we're going to call it yet...</p>
        <h2>What are some inspirations for your music?</h2>
        <p>Ironically we all have different music tastes when it comes to metal and rock. We believe that this help us blend each of our own personal style into the songs we make to make them as unique as possible. Our tastes come from as wide a range Audioslave, a band that Josh likes, to Darko US, a band that Logan likes. Don't forget everything in between like Enox, Bloodsimple, and Five Finger Death Punch. We're inspired by other bands as well our own lives.</p>
